print("Bem-vindo ao calculador top de idades para votar... ")

# Pergunto a idade e guardo numa variável
idade = int(input("Digite a sua idade EM NÚMEROS: "))

# Condicionais... if = fazer uma comparação que pode ou não ser verdadeira
if (idade >= 16):
    # se for verdade a informação, executa este código
    print("Parabéns, você pode decidir o futuro da nação")
else:
    # se não for verdadeira executa este outro trecho
    print("Volte pra casa jovem gafanhoto")