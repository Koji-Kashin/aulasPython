# Faça um programa queleia dois números distintos e os guarde em variáveis

print("Olá, boa noite!!")

priNum = float(input("Digite um número número: "))
segNum = float(input("Digite outro número: "))

# Operadores Relacionais...

print("Vamos comparar o números escolhidos...")
print (priNum > segNum, "\n")
print (priNum < segNum, "\n")
print (priNum >= segNum, "\n")
print (priNum <= segNum, "\n")
print (priNum == segNum, "\n")
print (priNum != segNum, "\n")
print (priNum > segNum, "\n")

